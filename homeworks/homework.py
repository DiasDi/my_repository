import math

def cm_to_m(number):
    meter = number // 100
    print(f'{number} cm is equal to {meter} meter')

def kg_to_tonn(number):
    tonn = number // 1000
    print(f'{number} kg is equal to {tonn} tonn')

def byte_to_kilobyte(number):
    kilobyte = number // 1024
    print(f'{number} byte is equal to {kilobyte} kilobyte')

